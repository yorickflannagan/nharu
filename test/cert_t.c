int test_cert()
{
	NH_RV rv;
	NH_CERTIFICATE_HANDLER hCert = NULL, hCA = NULL;
	NH_ASN1_PNODE node = NULL;

	printf("Testing certificate parsing... ");
	if (NH_SUCCESS(rv = NH_parse_certificate(enduser_cert, sizeof(enduser_cert), &hCert)))
	{
		rv = hCert->check_validity(hCert, &instant);
		if (NH_SUCCESS(rv) && NH_SUCCESS(rv = NH_parse_certificate(endca_cert, sizeof(endca_cert), &hCA)))
		{
			if (!hCA->match_subject(hCA, hCert->issuer)) rv = NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->verify(hCert, hCA->pubkey);
			if (NH_SUCCESS(rv)) rv = hCert->version(hCert, &node);
			if (NH_SUCCESS(rv)) rv = (*(int*) node->child->value + 1 == version) ? NH_OK : NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->signature_mech(hCert, &node);
			if (NH_SUCCESS(rv)) rv = NH_match_oid(signature, NHC_OID_COUNT(signature), node->child->value, node->child->valuelen) ? NH_OK : NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->not_before(hCert, &node);
			if (NH_SUCCESS(rv)) rv = difftime(mktime(&notBefore), mktime(node->value)) == 0 ? NH_OK : NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->not_after(hCert, &node);
			if (NH_SUCCESS(rv)) rv = difftime(mktime(&notAfter), mktime(node->value)) == 0 ? NH_OK : NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->basic_constraints(hCert, &node);
			if (NH_SUCCESS(rv)) rv = (node && !ASN_IS_PRESENT(node->child)) ? NH_OK : NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->ext_key_usage(hCert, &node);
			if (NH_SUCCESS(rv) && NH_SUCCESS(rv = (node && (node = node->child)) ? NH_OK : NH_PKIX_ERROR))
			{
				rv = NH_match_oid(clientAuth, NHC_OID_COUNT(clientAuth), node->value, node->valuelen) ? NH_OK : NH_PKIX_ERROR;
				if (NH_SUCCESS(rv) && NH_SUCCESS(rv = (node = node->next) ? NH_OK : NH_PKIX_ERROR)) rv = NH_match_oid(emailProctection, NHC_OID_COUNT(emailProctection), node->value, node->valuelen) ? NH_OK : NH_PKIX_ERROR;
				if (NH_SUCCESS(rv) && NH_SUCCESS(rv = (node = node->next) ? NH_OK : NH_PKIX_ERROR)) rv = NH_match_oid(msLogon, NHC_OID_COUNT(msLogon), node->value, node->valuelen) ? NH_OK : NH_PKIX_ERROR;
			}
			if (NH_SUCCESS(rv)) rv = hCert->aki(hCert, &node);
			if (NH_SUCCESS(rv)) rv = (node && node->child && node->child->valuelen == sizeof(aki) && memcmp(node->child->value, aki, node->valuelen) == 0) ? NH_OK : NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->ski(hCert, &node);
			if (NH_SUCCESS(rv)) rv = (node && node->valuelen == sizeof(ski) && memcmp(node->value, ski, node->valuelen) == 0) ? NH_OK : NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->key_usage(hCert, &node);
			if (NH_SUCCESS(rv)) rv = (node && *((NH_PBITSTRING_VALUE) node->value)->string == keyUsage) ? NH_OK : NH_PKIX_ERROR;
			if (NH_SUCCESS(rv)) rv = hCert->subject_alt_names(hCert, &node);
			if (NH_SUCCESS(rv)) rv = (node && node->child && node->child->size + node->child->contents - node->child->identifier == sizeof(oName_1) && memcmp(node->child->identifier, oName_1, sizeof(oName_1)) == 0) ? NH_OK : NH_PKIX_ERROR;
			NH_release_certificate(hCA);
		}
		NH_release_certificate(hCert);
	}
	if (NH_SUCCESS(rv)) printf("Done!\n");
	else printf("Failed\n");
	return rv;
}


