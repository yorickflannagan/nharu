{
	NH_RSA_PRIVKEY_HANDLER hHandler = (NH_RSA_PRIVKEY_HANDLER) params;
	return hHandler->sign(hHandler, mechanism, data->data, data->length, signature, sigSize);
}
int test_cms_signed_data_with_pubkey(char *szCert)
{
	NH_RV  rv;
	unsigned char *pCert = NULL;
	int certlen;

	NH_RSA_PUBKEY_HANDLER hPubKey = NULL;
	NH_RSA_PRIVKEY_HANDLER hPrivKey = NULL;
	NH_CMS_SD_ENCODER hCMSEncoder = NULL;
	NH_CERTIFICATE_HANDLER hCert = NULL;
	NH_CMS_ISSUER_SERIAL_STR sid = { NULL, NULL, NULL };
	unsigned char *encoding = NULL;
	size_t size;
	NH_CMS_SD_PARSER hCMSParser = NULL;

	printf("%s", "Testing CMS SignedData encoding with RSA public key validation... ");
	if
	(
		NH_SUCCESS(rv = load_file(szCert, &pCert, &certlen)) &&
		NH_SUCCESS(rv = NH_parse_certificate(pCert, certlen, &hCert))
	)
	{
		sid.name = hCert->issuer;
		sid.serial = hCert->serialNumber;
		rv = NH_generate_RSA_keys(2048, 65537, &hPubKey, &hPrivKey);
	}
	if (NH_SUCCESS(rv)) rv = NH_cms_encode_signed_data(&eContent, &hCMSEncoder);
	if (NH_SUCCESS(rv)) rv = hCMSEncoder->data_ctype(hCMSEncoder, CK_TRUE);
	if (NH_SUCCESS(rv)) rv = hCMSEncoder->sign(hCMSEncoder, &sid, CKM_SHA1_RSA_PKCS, sign_callback, hPrivKey);
	if (NH_SUCCESS(rv)) size = hCMSEncoder->hEncoder->encoded_size(hCMSEncoder->hEncoder, hCMSEncoder->hEncoder->root);
	if (NH_SUCCESS(rv)) rv = (encoding = (unsigned char*)malloc(size)) ? NH_OK : NH_OUT_OF_MEMORY_ERROR;
	if (NH_SUCCESS(rv)) rv = hCMSEncoder->hEncoder->encode(hCMSEncoder->hEncoder, hCMSEncoder->hEncoder->root, encoding);
	if (NH_SUCCESS(rv)) rv = NH_cms_discover(encoding, size) == NH_SIGNED_DATA_CTYPE ? NH_OK : NH_UNEXPECTED_ENCODING;
	if (NH_SUCCESS(rv)) rv = NH_cms_parse_signed_data(encoding, size, &hCMSParser);
	if (NH_SUCCESS(rv)) rv = hCMSParser->verify_rsa(hCMSParser, 0, hPubKey);
	if (NH_SUCCESS(rv)) rv = hCMSParser->validate_attached(hCMSParser);

	if (pCert) free(pCert);
	if (hCert) NH_release_certificate(hCert);
	if (hPubKey) NH_release_RSA_pubkey_handler(hPubKey);
	if (hPrivKey) NH_release_RSA_privkey_handler(hPrivKey);
	if (hCMSEncoder) NH_cms_release_sd_encoder(hCMSEncoder);
	if (encoding) free(encoding);
	if (hCMSParser) NH_cms_release_sd_parser(hCMSParser);

	if (NH_SUCCESS(rv)) printf("%s\n", "succeeded!");
	else printf("failed with error code %lu\n", rv);
	return rv;
}


