int test_cms_signed_data(char *szSigner, char *szCA)
{
	NH_RV  rv;
	unsigned char *pSigner = NULL, *pCA = NULL;
	int signerlen, calen;
	NH_CMS_SD_ENCODER hCMSEncoder = NULL;
	NH_CERTIFICATE_HANDLER hCert = NULL;
	NH_CMS_ISSUER_SERIAL_STR sid = { NULL, NULL, NULL }, *aSid = NULL;
	unsigned char *encoding = NULL;
	size_t size;
	NH_CMS_SD_PARSER hCMSParser = NULL;


	printf("%s", "Testing CMS SignedData encoding... ");
	if
	(
		NH_SUCCESS(rv = load_file(szSigner, &pSigner, &signerlen)) &&
		NH_SUCCESS(rv = load_file(szCA, &pCA, &calen)) &&
		NH_SUCCESS(rv = NH_cms_encode_signed_data(&eContent, &hCMSEncoder)) &&
		NH_SUCCESS(rv = NH_parse_certificate(pSigner, signerlen, &hCert))
	)
	{
		rv = hCMSEncoder->data_ctype(hCMSEncoder, CK_TRUE);
		if (NH_SUCCESS(rv)) rv = hCMSEncoder->add_cert(hCMSEncoder, hCert);
		if (NH_SUCCESS(rv))
		{
			sid.name = hCert->issuer;
			sid.serial = hCert->serialNumber;
			rv = hCMSEncoder->sign(hCMSEncoder, &sid, CKM_SHA1_RSA_PKCS, sign, NULL);
			if (NH_SUCCESS(rv))
			{
				size = hCMSEncoder->hEncoder->encoded_size(hCMSEncoder->hEncoder, hCMSEncoder->hEncoder->root);
				rv = (encoding = (unsigned char*)malloc(size)) ? NH_OK : NH_OUT_OF_MEMORY_ERROR;
				if (NH_SUCCESS(rv)) rv = hCMSEncoder->hEncoder->encode(hCMSEncoder->hEncoder, hCMSEncoder->hEncoder->root, encoding);
			}
		}
	}
	if (NH_SUCCESS(rv)) printf("%s\n", "succeeded!");
	else printf("failed with error code %lu\n", rv);

	if (NH_SUCCESS(rv))
	{
		printf("%s", "Testing CMS SignedData parsing... ");
		rv = NH_cms_discover(encoding, size) == NH_SIGNED_DATA_CTYPE ? NH_OK : NH_CMS_ERROR;
		if (NH_SUCCESS(rv)) rv = NH_cms_parse_signed_data(encoding, size, &hCMSParser);
		if (NH_SUCCESS(rv)) rv = hCMSParser->get_sid(hCMSParser, 0, &aSid);
		if (NH_SUCCESS(rv)) rv = hCMSParser->get_cert(hCMSParser, aSid, &hCert);
		if (NH_SUCCESS(rv)) rv = hCMSParser->verify(hCMSParser, 0, hCert->pubkey);
		if (NH_SUCCESS(rv)) rv = hCMSParser->validate_attached(hCMSParser);
	}
	if (NH_SUCCESS(rv)) printf("%s\n", "succeeded!");
	else printf("failed with error code %lu\n", rv);

	if (pSigner) free(pSigner);
	if (pCA) free(pCA);
	if (hCMSEncoder) NH_cms_release_sd_encoder(hCMSEncoder);
	if (hCert) NH_release_certificate(hCert);
	if (encoding) free(encoding);
	if (hCMSParser) NH_cms_release_sd_parser(hCMSParser);
	return (int)rv;
}

